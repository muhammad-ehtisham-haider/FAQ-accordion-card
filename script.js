var rotation = 0;
function rotateImage() {
  rotation += 180; 
  if (rotation === 360) { 

    rotation = 0;
  }
  document.querySelector("#arrowOne").style.transform = `rotate(${rotation}deg)`;

}

function setProperty() {
    var x = document.getElementById("ans-one") ;
    if (x.style.display === "none") {
        x.style.display = "block" ;
    } else {
        x.style.display = "none" ;
    }
}



